package rml.model;

public class Cell {
	
	private String id;
	private String betrainid;
	private int numfortrain;
	private int chairnum;
	private int record;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getBetrainid() {
		return betrainid;
	}
	public void setBetrainid(String betrainid) {
		this.betrainid = betrainid;
	}
	public int getNumfortrain() {
		return numfortrain;
	}
	public void setNumfortrain(int numfortrain) {
		this.numfortrain = numfortrain;
	}
	public int getChairnum() {
		return chairnum;
	}
	public void setChairnum(int chairnum) {
		this.chairnum = chairnum;
	}
	public int getRecord() {
		return record;
	}
	public void setRecord(int record) {
		this.record = record;
	}
	
}