package rml.model;

public class Chair {
	
	private String id;
	private String becellid;
	private int numforcell;
	private int record;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getBecellid() {
		return becellid;
	}
	public void setBecellid(String becellid) {
		this.becellid = becellid;
	}
	public int getNumforcell() {
		return numforcell;
	}
	public void setNumforcell(int numforcell) {
		this.numforcell = numforcell;
	}
	public int getRecord() {
		return record;
	}
	public void setRecord(int record) {
		this.record = record;
	}

}
